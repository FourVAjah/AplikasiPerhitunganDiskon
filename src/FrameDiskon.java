    private void btnHitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHitungActionPerformed
        try {
            double hargaAsli = Double.parseDouble(txtHargaAsli.getText());
            int diskonPersen = Integer.parseInt(cmbDiskon.getSelectedItem().toString().replace("%", ""));
            int diskonSlider = sldDiskon.getValue();

            // Ambil diskon maksimum (antara combobox dan slider)
            int diskonFinal = Math.max(diskonPersen, diskonSlider);

            // Tambahan diskon jika pakai kode kupon
            String kupon = txtKupon.getText().trim();
            if (kupon.equalsIgnoreCase("HEMAT5")) {
                diskonFinal += 5;
            } else if (kupon.equalsIgnoreCase("SUPER10")) {
                diskonFinal += 10;
            }

            // Batas maksimum 70%
            if (diskonFinal > 70) diskonFinal = 70;

            double jumlahDiskon = hargaAsli * diskonFinal / 100;
            double hargaAkhir = hargaAsli - jumlahDiskon;

            txtHargaAkhir.setText(String.format("Rp %.2f", hargaAkhir));

            // Tambahkan ke riwayat
            txtRiwayat.append("Harga: Rp" + hargaAsli +
                " | Diskon: " + diskonFinal + "% | Hemat: Rp" + jumlahDiskon + "\n");

        } catch (NumberFormatException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Masukkan harga asli dengan benar!");
        }
    }//GEN-LAST:event_btnHitungActionPerformed

